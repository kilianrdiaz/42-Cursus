char	*ft_strchr(const char *str, int c)
{
	int		i;
	char	a;

	i = 0;
	a = c;
	while (str[i])
	{
		if (str[i] == a)
			return ((char *)&str[i]);
		i++;
	}
	if (str[i] == a)
		return ((char *)&str[i]);
	return (0);
}
