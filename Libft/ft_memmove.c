void	*ft_memmove(void *s1, const void *s2, size_t n)
{
	int		i;
	char	*temp;

	i = 0;
	while (i < n)
	{
		temp[i] = ((char *)s2[i]);
		i++;
	}
	i = 0;
	while (i < n)
	{
		((char *)s1[i]) = temp[i];
		i++;
	}
	return (s1);
}
